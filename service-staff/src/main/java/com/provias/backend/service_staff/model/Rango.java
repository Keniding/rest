package com.provias.backend.service_staff.model;

public enum Rango {
    INTERN,         // Estudiante o en prácticas
    TRAINEE,        // En formación o recién ingresado
    ASSOCIATE,      // Nivel inicial
    JUNIOR,         // Experiencia básica
    SENIOR,         // Experiencia significativa
    LEAD,           // Lidera equipos o proyectos
    SPECIALIST,     // Experto en un área
    COORDINATOR,    // Coordina actividades
    MANAGER,        // Gestiona equipo o departamento
    PRINCIPAL,      // Alto nivel de expertise
    CONSULTANT,     // Asesoramiento especializado
    DIRECTOR,       // Dirige un área importante
    VICE_PRESIDENT, // Ejecutivo bajo Director
    EXECUTIVE,      // Toma decisiones estratégicas
    CHIEF,          // Nivel C-suite
    PRESIDENT,      // Responsable de una división
    PARTNER,        // Propietario o líder
    FELLOW          // Alto nivel académico
}
